# 1. Работа со словарями
# Создаем словарь my_dict с именами и годами рождения
my_dict = {
    "Анатолий": 1988,  # Ваше имя и год рождения
    "Иван": 1990,
    "Светлана": 1985,
}

# Выводим на экран словарь my_dict
print("Dict:", my_dict)

# 2. Выводим одно значение по существующему ключу
print("Existing value:", my_dict["Анатолий"])

# 3. Попробуем получить значение по отсутствующему ключу
# Вместо ошибки выведем None
print("Not existing value:", my_dict.get("Петр", None))

# 4. Добавляем две произвольные пары в словарь
my_dict["Елена"] = 1995
my_dict["Алексей"] = 1993

# 5. Удаляем одну из пар по существующему ключу и выводим значение
deleted_value = my_dict.pop("Иван")  # Удаляем ключ "Иван"
print("Deleted value:", deleted_value)

# 6. Выводим измененный словарь
print("Modified dictionary:", my_dict)

# 7. Работа с множествами
# Создаем множество my_set с разными типами данных
my_set = {1, "Яблоко", 42.314, "Яблоко", (1, 2)}  # Повторяющееся значение "Яблоко"

# Выводим на экран множество my_set
print("Set:", my_set)

# 8. Добавляем 2 произвольных элемента в множество
my_set.add(13)  # Добавляем целое число
my_set.add("Апельсин")  # Добавляем строку

# 9. Удаляем один любой элемент из множества
my_set.discard(1)  # Удаляем элемент 1 (если он есть)

# 10. Выводим измененное множество
print("Modified set:", my_set)